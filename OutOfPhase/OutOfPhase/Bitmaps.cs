    public class ManagedBitmap2
    {
        private readonly byte[] raw; // top to bottom, left to right, monochrome bits, "stride" bytes per row
        private readonly int width;
        private readonly int height;
        private readonly int stride; // i.e. row length in bytes

        public int Width { get { return width; } }
        public int Height { get { return height; } }

        public ManagedBitmap2(int width, int height)
        {
            this.width = width;
            this.height = height;
            this.stride = (width + 7) / 8;
            raw = new byte[stride * height];
        }

        public ManagedBitmap2(int width, int height, byte[] raw)
        {
            this.width = width;
            this.height = height;
            this.stride = (width + 7) / 8;
            if (raw.Length != stride * height)
            {
                throw new ArgumentException();
            }
            this.raw = (byte[])raw.Clone();
        }

        public static ManagedBitmap2 Or(ManagedBitmap2 a, ManagedBitmap2 b)
        {
            if ((a.width != b.width) || (a.height != b.height))
            {
                throw new ArgumentException();
            }

            ManagedBitmap2 result = new ManagedBitmap2(a.width, a.height, a.raw/*cloned*/);
            for (int i = 0; i < a.raw.Length; i++)
            {
                result.raw[i] |= b.raw[i];
            }
            return result;
        }

        public Bitmap ToGDI(Color level0, Color level1)
        {
            Bitmap native = new Bitmap(width, height, PixelFormat.Format1bppIndexed);

            ColorPalette palette = native.Palette;
            palette.Entries[0] = level0;
            palette.Entries[1] = level1;
            native.Palette = palette;

            BitmapData nativeData = null;
            try
            {
                nativeData = native.LockBits(new Rectangle(0, 0, width, height), ImageLockMode.WriteOnly, PixelFormat.Format1bppIndexed);
                if (nativeData.Stride < 0)
                {
                    Debug.Assert(false);
                    throw new NotSupportedException();
                }

                for (int row = 0; row < height; row++)
                {
                    Marshal.Copy(raw, row * stride, new IntPtr(nativeData.Scan0.ToInt64() + row * nativeData.Stride), stride);
                }
            }
            finally
            {
                if (nativeData != null)
                {
                    native.UnlockBits(nativeData);
                }
            }
            return native;
        }

#if DEBUG
        public override string ToString()
        {
            char[] cc = new char[(width + 2) * height];
            int c = 0;
            for (int i = 0; i < height; i++)
            {
                for (int j = 0; j < width; j++)
                {
                    cc[c++] = ((raw[i * stride + (j >> 3)] & (128 >> (j & 7))) == 0) ? ' ' : '*';
                }
                cc[c++] = '\r';
                cc[c++] = '\n';
            }
            return new String(cc);
        }
#endif
    }

