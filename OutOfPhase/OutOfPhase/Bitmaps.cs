        [DllImport("user32.dll")]
        private static extern IntPtr CreateCursor(IntPtr hInst, int xHotSpot, int yHotSpot, int nWidth, int nHeight, IntPtr pvANDPlane, IntPtr pvXORPlane);

        // https://msdn.microsoft.com/en-us/library/windows/desktop/ms648380%28v=vs.85%29.aspx
        // https://msdn.microsoft.com/en-us/library/windows/desktop/ms648385%28v=vs.85%29.aspx
        //
        // Win32 API:
        // AND mask  XOR mask  Display
        // ----------------------------------
        //    0         0      Black
        //    0         1      White
        //    1         0      Screen
        //    1         1      Reverse screen
        //
        // Old Macintosh behavior:
        //  Image      Mask    Display
        //    0         0      Screen
        //    0         1      White
        //    1         0      Reverse screen
        //    1         1      Black
        private static Cursor MakeCursor(ushort[] rawImage, ushort[] rawMask, int hotX, int hotY)
        {
            if (rawImage.Length != rawMask.Length)
            {
                throw new ArgumentException();
            }

            int c = rawImage.Length;
            ushort[] rawAndPlane = new ushort[c];
            ushort[] rawXorPlane = new ushort[c];
            for (int i = 0; i < c; i++)
            {
                rawAndPlane[i] = (ushort)~rawMask[i];
                rawXorPlane[i] = (ushort)(rawImage[i] ^ rawMask[i]);

                rawAndPlane[i] = (ushort)((rawAndPlane[i] >> 8) | (rawAndPlane[i] << 8));
                rawXorPlane[i] = (ushort)((rawXorPlane[i] >> 8) | (rawXorPlane[i] << 8));
            }

            GCHandle rawXorPlaneH = GCHandle.Alloc(rawXorPlane, GCHandleType.Pinned);
            GCHandle rawAndPlaneH = GCHandle.Alloc(rawAndPlane, GCHandleType.Pinned);

            IntPtr hCursor = CreateCursor(
                Marshal.GetHINSTANCE(Assembly.GetAssembly(typeof(Bitmaps)).GetModules()[0]),
                hotX,
                hotY,
                16,
                16,
                rawAndPlaneH.AddrOfPinnedObject(),
                rawXorPlaneH.AddrOfPinnedObject());

            rawXorPlaneH.Free();
            rawAndPlaneH.Free();

            return new Cursor(hCursor);
        }
